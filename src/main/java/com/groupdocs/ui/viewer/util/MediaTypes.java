    public static MediaTypes[] getAllMediaTypesForExtension(String extension) {
        if (extension == null) {
            throw new IllegalArgumentException("extension");
        }
        List<MediaTypes> mediaTypes = new ArrayList<>();
        for (MediaTypes mediaType : MediaTypes.values()) {
            if (extension.equals(mediaType.mExtension)) {
                mediaTypes.add(mediaType);
            }
        }
        return mediaTypes.toArray(new MediaTypes[0]);
    }

    public static String[] getAllExtensionsForMediaType(String mediaType) {
        if (mediaType == null) {
            throw new IllegalArgumentException("mediaType");
        }
        List<String> mediaTypes = new ArrayList<>();
        for (MediaTypes mediaTypeObj : MediaTypes.values()) {
            if (mediaType.equals(mediaTypeObj.mType)) {
                mediaTypes.add(mediaTypeObj.mExtension);
            }
        }
        return mediaTypes.toArray(new String[0]);
    }

    public static String detectMediaTypeForWeb(String extension) {
        if (extension == null) {
            throw new IllegalArgumentException("extension");
        }
        final List<MediaTypes> mediaTypes = new ArrayList<>(Arrays.asList(MediaTypes.values()));
        mediaTypes.remove(MediaTypes.ATOM_SYNDICATION_FORMAT_XML);
        mediaTypes.remove(MediaTypes.RSS_REALLY_SIMPLE_SYNDICATION_XML);
        mediaTypes.remove(MediaTypes.JPG_IMAGE_CITRIX_CLIENT);
        mediaTypes.remove(MediaTypes.JPEG_IMAGE_CITRIX_CLIENT);
        mediaTypes.remove(MediaTypes.PORTABLE_NETWORK_GRAPHICS_PNG_X_TOKEN);
        mediaTypes.remove(MediaTypes.PORTABLE_NETWORK_GRAPHICS_PNG_CITRIX_CLIENT);
        for (MediaTypes mediaType : mediaTypes) {
            if (extension.equals(mediaType.mExtension)) {
                return mediaType.mType;
            }
        }
        return BINARY_DATA.mType;
    }

